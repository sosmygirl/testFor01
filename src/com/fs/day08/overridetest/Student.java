package com.fs.day08.overridetest;

public class Student {
    //name 名字，age 年龄
    private String name;
    private int age;
    //getter setter 方法设置年龄

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
